const {random} = require('../../../Utils');

const LEET = {
	'a': ['@'],
	'A': ['4'],
	'B': ['8'],
	'c': [':copyright:'],
	'C': [':copyright:'],
	'e': ['3'],
	'E': ['3'],
	'F': ['|='],
	'g': ['9'],
	'H': ['|-|','#'],
	'i': ['!'],
	'I': ['|'],
	'J': ['_)'],
	'k': ['|<','|{'],
	'K': ['|<','|{'],
	'l': ['1'],
	'L': ['1','|_'],
	'M': ['|\\/|','(\\/)'],
	'N': ['|\\|'],
	'o': ['0'],
	'O': ['0'],
	'p': ['|*'],
	'P': ['|*'],
	'q': ['*|'],
	's': ['5','$'],
	'S': ['5','$'],
	't': ['+','7'],
	'T': ['7'],
	'U': ['(_)'],
	'v': ['\\/'],
	'V': ['\\/'],
	'w': ['\\/\\/','vv'],
	'W': ['\\/\\/','VV'],
	'x': ['><'],
	'X': ['><']
};

module.exports = function leet(x) {
	return x.split('').map(c => c in LEET ? random(LEET[c]) : c).join('');
};
